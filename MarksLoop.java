public class MarksLoop {
    public static void main(String[] args) {
        int marks[]={56,67,79,46};
        System.out.println("The marks of the student is:");
        for(int i=0;i<marks.length;i++)
        {
            System.out.println("marks is:" +(i+1)+":" +marks[i]);
        }
    }
}
